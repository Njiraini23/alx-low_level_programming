#include <stdio.h>

/**
*op_add -function to return sume of values
*@a: First argument
*@b: Second argument
*Return: The sum of two arguments
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
*op_sub -Function that returns the difference of 2 values
*@a: first argument
*@b: second argument
*Return: THe difference of two arguments
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
*op_mul - Function to return product of 2 values
*@: First argument
*@b: second argument
*
*Return: The product of two values
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
*op_div -Function to return quotient of values
*@a: First argument
*@b: Second argument
*Return: The quotient of values
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod -Function to return reminder of values
*@a: First argument
*@b: Second argument
*Return: The reminder of two values
*/
int op_mod(int a, int b)
{
	return (a % b);
}
